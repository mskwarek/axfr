result_dir=$1
write_dir=$2

fileArr=`find $result_dir -type f`

for file in ${fileArr[@]}; do

    grep  -P '\t28\t' $file | cut -d$'\t' -f 4 | grep -E -o '(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))' | uniq >> $write_dir/ipv6.result
    grep -o '\S*git\.\S*' $file >> $write_dir/git.result
    grep -o '\S*jenkins\.\S*' $file >> $write_dir/jenkins.result
    grep -o '\S*svn\.\S*' $file >> $write_dir/svn.result
    grep 'v=spf' $file >> $write_dir/spf.result
    echo $file | rev | cut -d '/' -f 1 | rev | cut -d _ -f 1 | rev | cut -d . -f 1 | rev >> $write_dir/tld.result
done
